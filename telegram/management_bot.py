    async def _handle_check_contract(self, message: Message) -> None:
        """Handle /check command"""
        try:
            # Expected format: /check CONTRACT_ADDRESS
            _, contract_address = message.text.split()
            
            contract_info = await self.solana.get_contract_info(contract_address)
            
            if not contract_info:
                await message.reply("‚ùå –ö–æ–Ω—Ç—Ä–∞–∫—Ç –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ")
                return
            
            info_text = (
                f"üìÑ –Ü–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—è –ø—Ä–æ –∫–æ–Ω—Ç—Ä–∞–∫—Ç:\n"
                f"–ê–¥—Ä–µ—Å–∞: {contract_info['address']}\n"
                f"–ë–∞–ª–∞–Ω—Å: {contract_info['balance']} SOL\n"
                f"–í–ª–∞—Å–Ω–∏–∫: {contract_info['owner']}\n"
                f"–í–∏–∫–æ–Ω—É–≤–∞–Ω–∏–π: {'–¢–∞–∫' if contract_info['executable'] else '–ù—ñ'}"
            )
            
            await message.reply(info_text)
            
        except ValueError:
            await message.reply("‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–∏–π —Ñ–æ—Ä–º–∞—Ç –∫–æ–º–∞–Ω–¥–∏. –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π—Ç–µ: /check CONTRACT_ADDRESS")
        except Exception as e:
            await message.reply(f"‚ùå –ü–æ–º–∏–ª–∫–∞: {str(e)}")
    
