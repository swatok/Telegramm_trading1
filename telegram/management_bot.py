            "–î–æ—Å—Ç—É–ø–Ω—ñ –∫–æ–º–∞–Ω–¥–∏:\n"
            "/open TOKEN AMOUNT - –í—ñ–¥–∫—Ä–∏—Ç–∏ –Ω–æ–≤—É –ø–æ–∑–∏—Ü—ñ—é\n"
